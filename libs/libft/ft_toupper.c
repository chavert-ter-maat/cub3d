/* ************************************************************************** */
/* ft_toupper() converts a lower-case to the corresponding upper-case letter. */
/* ************************************************************************** */

#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	return (c);
}
