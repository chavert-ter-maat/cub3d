/* ************************************************************************** */
/* ft_isalnum() function returns zero if the character tests false and 	  	  */
/* returns non-zero if the character tests true.							  */
/* ************************************************************************** */

#include "libft.h"

int	ft_isalnum(int a)
{
	return ((a >= '0' && a <= '9') || (a >= 'A' && a <= 'Z')
		|| (a >= 'a' && a <= 'z'));
}
